void cmd_l( unsigned char cmd )	
{
	SetBit(PORTE,5);	// E
	_delay_us(10);
	ClrBit(PORTE,4);	// RS
	ClrBit(PORTE,6);	// CS2
	SetBit(PORTE,7);	// CS1
	PORTA = cmd ;		
	_delay_us(10);
	ClrBit(PORTE,5);	// E
}

void cmd_r( unsigned char cmd )
{
	SetBit(PORTE,5);	// E
	_delay_us(10);
	ClrBit(PORTE,4);	// RS
	SetBit(PORTE,6);	// CS2
	ClrBit(PORTE,7);	// CS1
	PORTA = cmd ;
	_delay_us(10);
	ClrBit(PORTE,5);	// E
}

void cmd_lr( unsigned char cmd )
{
	SetBit(PORTE,5);	// E
	_delay_us(10);
	ClrBit(PORTE,4);	// RS
	SetBit(PORTE,6);	// CS2
	SetBit(PORTE,7);	// CS1
	PORTA = cmd ;
	_delay_us(10);
	ClrBit(PORTE,5);	// E
}

void data_l( unsigned char data )          
{
	SetBit(PORTE,5);	// E
	_delay_us(10);
	SetBit(PORTE,4);	// RS
	ClrBit(PORTE,6);	// CS2
	SetBit(PORTE,7);	// CS1
	PORTA = data ; 
	_delay_us(10);
	ClrBit(PORTE,5);	// E
}

void data_r( unsigned char data )
{
	SetBit(PORTE,5);	// E
	_delay_us(10);
	SetBit(PORTE,4);	// RS
	SetBit(PORTE,6);	// CS2
	ClrBit(PORTE,7);	// CS1
	PORTA = data ;
	_delay_us(10);
	ClrBit(PORTE,5);	// E
}

void data_lr( unsigned char data )
{
	SetBit(PORTE,5);	// E
	_delay_us(10);
	SetBit(PORTE,4);	// RS
	SetBit(PORTE,6);	// CS2
	SetBit(PORTE,7);	// CS1
	PORTA = data ;
	_delay_us(10);
	ClrBit(PORTE,5);	// E
}

void display_char(unsigned char x, unsigned char y, unsigned char character)
{
	unsigned char i;
	
	cmd_lr(0xB8+x);     // x address
	if( y < 10 )
		cmd_l(0x40+y*6+4); // y address	
	else
		cmd_r(0x40+(y-10)*6); // y address
		
	for(i = 0; i < 5; i++)
	{
		if(y < 10)                 
			data_l(font[character-0x20][i]);
		else                           
			data_r(font[character-0x20][i]);
	}			
		
	if(y < 10)
		data_l(0x00);                   
	else
		data_r(0x00); 				                  	
}

void display_string(unsigned char x, unsigned char y, unsigned char *string)
{
	unsigned char i;
	
	cmd_lr(0xB8+x);     // x address
	if( y < 10 )
		cmd_l(0x40+y*6+4); // y address
	else
		cmd_r(0x40+(y-10)*6); // y address
	
	while( *string != '\0' ){
		if( y == 10 ){
			cmd_r(0x40);
		}
		for(i = 0; i < 5; i++){
			if(y < 10)					   
				data_l(font[*string-0x20][i]);
			else                           
				data_r(font[*string-0x20][i]);
		}
	
		if(y < 10)
			data_l(0x00);                    
		else
			data_r(0x00);
			
		string++;
		y++;
	}	
}

void lcd_clear()
{
	unsigned char i, j, x, y;
	x = 0xB8;                         /* X start address */
	y = 0x40;                         /* Y start address */
	for(i = 0; i < 8; i++)
	{
		cmd_lr(x);    cmd_lr(y);
		for(j = 0; j < 64; j++)
			data_lr(0x00);           /* clear CS1 and CS2 */
		x++;
	}
	//HW: Clear ScreenBuffer as well
}

void fill_char_xy(unsigned char x_char, unsigned char y, unsigned char data)
{
	cmd_lr( 0xB8 + x_char);         // X address
	if(y <= 63)
	{
		cmd_l( 0x40 + y);      // CS1 Y address
		data_l(data);
	}
	else
	{
		cmd_r( 0x40 + y - 64); // CS2 Y address
		data_r(data);
	}
}

void InitScreenBuffer( unsigned char ScreenBuffer[8][128] )
{
	unsigned int i, j;
	
	for(i = 0 ; i < 8; i++){
		for(j = 0 ; j < 128; j++){
			ScreenBuffer[i][j] = 0;
		}			
	}
}

void GLCD_Dot(unsigned char ScreenBuffer[8][128], unsigned char x, unsigned char y)
{
	unsigned char x_char, xx, dot_data;
	
	//Error: out of screen
	if((x >= 64) || (y >= 128)) return; 
	
	x_char = x / 8;    // calculate x address (in terms of char)
	xx = x % 8;        //                     (within a char)
	
	dot_data = (1<<xx);                  // Make a dot
	ScreenBuffer[x_char][y] |= dot_data; // OR old data with dot data
	// draw on GLCD screen
	fill_char_xy(x_char, y, ScreenBuffer[x_char][y]);          
}

void GLCD_Line(unsigned char ScreenBuffer[8][128], unsigned char x1,unsigned char y1,unsigned char x2,unsigned char y2)
{
	int x, y;
	if(y1 != y2)    // if y1 != y2, y is variable
	{
		if(y1 < y2)    //              x is function
		{
			for(y = y1; y <= y2; y++)
			{
				x = x1 + (y - y1)*(x2 - x1)/(y2 - y1);
				GLCD_Dot(ScreenBuffer,x,y);
			}
		}
		else
		{
			for(y = y1; y >= y2; y--)
			{
				x = x1 + (y - y1)*(x2 - x1)/(y2 - y1);
				GLCD_Dot(ScreenBuffer,x,y);
			}
		}
	}
	else if(x1 != x2)   // if x1 != x2, x is variable
	{
		if(x1 < x2)    //              y is function
		{
			for(x = x1; x <= x2; x++)
			{ y = y1 + (x - x1)*(y2 - y1)/(x2 - x1);
				GLCD_Dot(ScreenBuffer,x,y);
			}
		}
		else
		{
			for(x = x1; x >= x2; x--)
			{
				y = y1 + (x - x1)*(y2 - y1)/(x2 - x1);
				GLCD_Dot(ScreenBuffer,x,y);
			}
		}
	}
	else                  // if x1 == x2 and y1 == y2,
	{
		GLCD_Dot(ScreenBuffer,x1,y1);  //             it is a dot
	}
}

